import crypto from "crypto";
import { Client } from "pg";
import dotenv from "dotenv";

dotenv.config();

const BASE_URL = process.env.SMOKE_BASE_URL || process.env.NEXT_PUBLIC_APP_URL || "http://127.0.0.1:3000";
const DATABASE_URL = process.env.SMOKE_DATABASE_URL || process.env.DATABASE_URL;

if (!DATABASE_URL) {
  console.error("Missing DATABASE_URL (or SMOKE_DATABASE_URL) for barter smoke test.");
  process.exit(1);
}

function deterministicReferenceId(namespace, id) {
  const hash = crypto.createHash("sha1").update(`${namespace}:${id}`).digest("hex").slice(0, 32).split("");

  hash[12] = "5";
  const variant = Number.parseInt(hash[16], 16);
  hash[16] = ((variant & 0x3) | 0x8).toString(16);

  return [
    hash.slice(0, 8).join(""),
    hash.slice(8, 12).join(""),
    hash.slice(12, 16).join(""),
    hash.slice(16, 20).join(""),
    hash.slice(20, 32).join(""),
  ].join("-");
}

function operationalExpenseBarterReferenceId(barterId) {
  return deterministicReferenceId("superapp:journal:operational-expense-barter:v1", barterId);
}

async function request(path, options = {}) {
  const response = await fetch(`${BASE_URL}${path}`, {
    ...options,
    headers: {
      "content-type": "application/json",
      ...(options.headers || {}),
    },
  });

  const text = await response.text();
  let body = null;

  if (text) {
    try {
      body = JSON.parse(text);
    } catch {
      body = text;
    }
  }

  return {
    ok: response.ok,
    status: response.status,
    body,
  };
}

async function requestOrThrow(path, options = {}) {
  const result = await request(path, options);
  if (!result.ok) {
    throw new Error(`HTTP ${result.status} ${path}: ${typeof result.body === "string" ? result.body : JSON.stringify(result.body)}`);
  }

  return result.body;
}

async function fetchSingleRow(db, query, params = []) {
  const result = await db.query(query, params);
  return result.rows[0] ?? null;
}

async function fetchRows(db, query, params = []) {
  const result = await db.query(query, params);
  return result.rows;
}

async function main() {
  const db = new Client({ connectionString: DATABASE_URL });
  await db.connect();

  let barterId = null;
  const failures = [];
  const checks = [];

  try {
    const expenseAccount = await fetchSingleRow(
      db,
      `SELECT id, code, name FROM accounting.accounts WHERE code = '61102' AND is_active = true LIMIT 1`
    );

    const inventoryRows = await fetchRows(
      db,
      `
        SELECT sb.inv_code, mi.inv_name, sb.qty_on_hand, mi.unit_cost
        FROM warehouse.stock_balances sb
        JOIN product.master_inventory mi
          ON mi.inv_code = sb.inv_code
        WHERE mi.is_active = true
          AND sb.qty_on_hand >= 1
        ORDER BY sb.qty_on_hand DESC, sb.inv_code ASC
        LIMIT 2
      `
    );

    if (!expenseAccount || inventoryRows.length < 2) {
      throw new Error("Seed data tidak cukup untuk barter smoke test.");
    }

    const [firstInventory, secondInventory] = inventoryRows;
    const initialBalances = new Map(inventoryRows.map((row) => [row.inv_code, Number(row.qty_on_hand)]));
    const today = new Date().toISOString().slice(0, 10);

    const createdBarter = await requestOrThrow("/api/accounting/operational-expense-barter", {
      method: "POST",
      body: JSON.stringify({
        barter_date: today,
        expense_account_id: expenseAccount.id,
        expense_label: "KOL Tukar Produk",
        description: "Smoke test opex barter multi-item",
        reference_no: "SMOKE-OPEX-BARTER",
        notes_internal: "Generated by smoke test",
      }),
    });
    barterId = createdBarter.id;

    await requestOrThrow(`/api/accounting/operational-expense-barter/${barterId}/items`, {
      method: "POST",
      body: JSON.stringify({
        inv_code: firstInventory.inv_code,
        qty: 1,
        unit_amount: String(Number(firstInventory.unit_cost) || 10000),
        notes: "Item 1",
      }),
    });

    const withTwoItems = await requestOrThrow(`/api/accounting/operational-expense-barter/${barterId}/items`, {
      method: "POST",
      body: JSON.stringify({
        inv_code: secondInventory.inv_code,
        qty: 1,
        unit_amount: String(Number(secondInventory.unit_cost) || 12000),
        notes: "Item 2",
      }),
    });

    const expectedTotal = withTwoItems.operational_expense_barter_items.reduce(
      (sum, item) => sum + Number(item.line_amount),
      0
    );

    const posted = await requestOrThrow(`/api/accounting/operational-expense-barter/${barterId}/post`, {
      method: "POST",
      body: JSON.stringify({}),
    });

    const referenceId = operationalExpenseBarterReferenceId(barterId);
    const journalSummary = await fetchSingleRow(
      db,
      `
        SELECT
          COUNT(DISTINCT je.id)::int AS journal_count,
          COUNT(jl.id)::int AS line_count,
          COALESCE(SUM(jl.debit), 0)::text AS total_debit,
          COALESCE(SUM(jl.credit), 0)::text AS total_credit
        FROM accounting.journal_entries je
        LEFT JOIN accounting.journal_lines jl
          ON jl.journal_entry_id = je.id
        WHERE je.reference_type = 'OPERATIONAL_EXPENSE_BARTER'
          AND je.reference_id = $1
      `,
      [referenceId]
    );

    const journalLines = await fetchRows(
      db,
      `
        SELECT a.code, COALESCE(jl.debit, 0)::text AS debit, COALESCE(jl.credit, 0)::text AS credit
        FROM accounting.journal_entries je
        JOIN accounting.journal_lines jl
          ON jl.journal_entry_id = je.id
        JOIN accounting.accounts a
          ON a.id = jl.account_id
        WHERE je.reference_type = 'OPERATIONAL_EXPENSE_BARTER'
          AND je.reference_id = $1
        ORDER BY a.code ASC, jl.id ASC
      `,
      [referenceId]
    );

    const movementRows = await fetchRows(
      db,
      `
        SELECT inv_code, qty_change, reference_type
        FROM warehouse.stock_movements
        WHERE reference_type = 'OPERATIONAL_EXPENSE_BARTER'
          AND reference_id = $1
        ORDER BY inv_code ASC, created_at ASC
      `,
      [barterId]
    );

    const afterPostBalances = await fetchRows(
      db,
      `
        SELECT inv_code, qty_on_hand
        FROM warehouse.stock_balances
        WHERE inv_code = ANY($1::varchar[])
        ORDER BY inv_code ASC
      `,
      [inventoryRows.map((row) => row.inv_code)]
    );

    checks.push({
      barter_id: barterId,
      status_after_post: posted.status,
      posted_total_amount: posted.total_amount,
      expected_total: expectedTotal,
      journal_count: journalSummary?.journal_count ?? 0,
      journal_line_count: journalSummary?.line_count ?? 0,
      journal_codes: journalLines.map((line) => line.code),
      movement_count: movementRows.length,
      movement_codes: movementRows.map((line) => line.inv_code),
    });

    if (posted.status !== "POSTED") {
      failures.push("Barter tidak masuk status POSTED.");
    }

    if (Number(posted.total_amount) !== expectedTotal) {
      failures.push("Total barter tidak sesuai akumulasi item.");
    }

    if ((journalSummary?.journal_count ?? 0) !== 1 || (journalSummary?.line_count ?? 0) !== 2) {
      failures.push("Jurnal barter tidak terbentuk tepat 1 header dan 2 line.");
    }

    if (!journalLines.some((line) => line.code === expenseAccount.code && Number(line.debit) > 0)) {
      failures.push("Jurnal barter tidak mendebit akun beban yang dipilih.");
    }

    if (!journalLines.some((line) => line.code === "13101" && Number(line.credit) > 0)) {
      failures.push("Jurnal barter tidak mengkredit akun persediaan 13101.");
    }

    if (movementRows.length !== 2 || !movementRows.every((line) => Number(line.qty_change) === -1)) {
      failures.push("Stock movement barter tidak terbentuk per item dengan qty negatif.");
    }

    for (const balance of afterPostBalances) {
      const before = initialBalances.get(balance.inv_code);
      if (before == null || Number(balance.qty_on_hand) !== before - 1) {
        failures.push(`Saldo stok ${balance.inv_code} tidak turun 1 setelah post.`);
      }
    }

    await requestOrThrow(`/api/accounting/operational-expense-barter/${barterId}/void`, {
      method: "POST",
      body: JSON.stringify({}),
    });

    const movementAfterVoid = await fetchSingleRow(
      db,
      `
        SELECT COUNT(*)::int AS movement_count
        FROM warehouse.stock_movements
        WHERE reference_type = 'OPERATIONAL_EXPENSE_BARTER'
          AND reference_id = $1
      `,
      [barterId]
    );

    const journalAfterVoid = await fetchSingleRow(
      db,
      `
        SELECT COUNT(*)::int AS journal_count
        FROM accounting.journal_entries
        WHERE reference_type = 'OPERATIONAL_EXPENSE_BARTER'
          AND reference_id = $1
      `,
      [referenceId]
    );

    const finalBalances = await fetchRows(
      db,
      `
        SELECT inv_code, qty_on_hand
        FROM warehouse.stock_balances
        WHERE inv_code = ANY($1::varchar[])
        ORDER BY inv_code ASC
      `,
      [inventoryRows.map((row) => row.inv_code)]
    );

    await requestOrThrow(`/api/accounting/operational-expense-barter/${barterId}`, {
      method: "DELETE",
    });
    barterId = null;

    if ((movementAfterVoid?.movement_count ?? 0) !== 0) {
      failures.push("Stock movement barter masih tersisa setelah void.");
    }

    if ((journalAfterVoid?.journal_count ?? 0) !== 0) {
      failures.push("Jurnal barter masih tersisa setelah void.");
    }

    for (const balance of finalBalances) {
      const before = initialBalances.get(balance.inv_code);
      if (before == null || Number(balance.qty_on_hand) !== before) {
        failures.push(`Saldo stok ${balance.inv_code} tidak kembali setelah void.`);
      }
    }

    console.log(JSON.stringify({
      ok: failures.length === 0,
      base_url: BASE_URL,
      failures,
      checks,
    }, null, 2));

    if (failures.length > 0) {
      process.exitCode = 1;
    }
  } finally {
    if (barterId) {
      try {
        await request(`/api/accounting/operational-expense-barter/${barterId}/void`, {
          method: "POST",
          body: JSON.stringify({}),
        });
      } catch {}

      try {
        await request(`/api/accounting/operational-expense-barter/${barterId}`, {
          method: "DELETE",
        });
      } catch {}
    }

    await db.end();
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
